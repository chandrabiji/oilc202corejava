package operators;

public class AssignmentOperators {

	public static void main(String[] args) {
		int a = 10;
		System.out.println(" A = "+a);
		a +=5;//a = a+5 = 10+5=15
		System.out.println(" A = "+a);
		a -=5;//a-5=15-5=10
		System.out.println(" A = "+a);
		a *=2;// a = 2*10=20
		System.out.println(" A = "+a);
		a /=2;
		System.out.println(" A = "+a);
		a %=2;
		System.out.println(" A = "+a);
		

	}

}
